/**
 * =============================================================================
 * НАЗВАНИЕ ЗАДАЧИ: 35. Search Insert Position (LeetCode)
 * СЛОЖНОСТЬ: Easy
 *
 * УСЛОВИЕ:
 * Дан ОТСОРТИРОВАННЫЙ массив уникальных целых чисел `nums` и число `target`.
 * Если `target` есть в массиве — верните его индекс.
 * Если нет — верните индекс, НА КОТОРЫЙ его нужно было бы вставить,
 * чтобы массив остался отсортированным.
 *
 * ВАЖНО: алгоритм должен работать за O(log n).
 *
 * Примеры:
 * Input:  nums = [1,3,5,6], target = 5
 * Output: 2      (5 найдена на индексе 2)
 *
 * Input:  nums = [1,3,5,6], target = 2
 * Output: 1      (2 нужно вставить между 1 и 3 → индекс 1)
 *
 * Input:  nums = [1,3,5,6], target = 7
 * Output: 4      (7 больше всех → вставить в конец → индекс 4)
 *
 * Ограничения:
 * - 1 <= nums.length <= 10^4
 * - -10^4 <= nums[i], target <= 10^4
 * - массив отсортирован по возрастанию, все значения уникальны
 * =============================================================================
 */

/**
 * @param {number[]} nums
 * @param {number} target
 * @return {number}
 */
var searchInsert = function (nums, target) {
  let left = 0; // левая граница поиска
  let right = nums.length - 1; // правая граница

  while (left <= right) {
    const mid = Math.floor((left + right) / 2); // середина

    if (nums[mid] === target) {
      return mid; // нашли!
    }
    if (nums[mid] < target) {
      left = mid + 1; // target правее → выкидываем левую половину
    } else {
      right = mid - 1; // target левее → выкидываем правую
    }
  }
  return left;
};

searchInsert([1, 3, 5, 6], 4);
