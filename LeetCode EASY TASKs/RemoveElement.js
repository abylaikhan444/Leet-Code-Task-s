/**
 * =============================================================================
 * НАЗВАНИЕ ЗАДАЧИ: 27. Remove Element (LeetCode)
 * СЛОЖНОСТЬ: Easy
 *
 * УСЛОВИЕ:
 * Дан массив целых чисел `nums` и число `val`.
 * Удалите ВСЕ вхождения `val` из `nums` IN-PLACE (в самом массиве,
 * без создания нового). Порядок оставшихся элементов может быть любым.
 * Верните количество элементов, НЕ равных `val` — назовём его k.
 *
 * Требования:
 * - Первые k элементов `nums` должны содержать все элементы, не равные `val`
 *   (в любом порядке).
 * - Что находится после индекса k-1 — не важно.
 * - Вернуть k.
 *
 * Примеры:
 * Input:  nums = [3,2,2,3], val = 3
 * Output: 2,  nums = [2,2,_,_]
 *
 * Input:  nums = [0,1,2,2,3,0,4,2], val = 2
 * Output: 5,  nums = [0,1,4,0,3,_,_,_]   (порядок может быть любым)
 *
 * Ограничения:
 * - 0 <= nums.length <= 100
 * - 0 <= nums[i] <= 50
 * - 0 <= val <= 100
 *
 * ВАЖНО: порядок НЕ важен (в отличие от задачи 26) — это сильно упрощает жизнь.
 * =============================================================================
 */

/**
 * @param {number[]} nums
 * @param {number} val
 * @return {number}
 */
var removeElement = function (nums, val) {
  let count = 0;
  for (let i = 0; i < nums.length; i++) {
    if (nums[i] === val) {
      nums[i] = "";
      count++;
    }
  }
  console.log(nums, count);
};

removeElement([3, 2, 2, 3], 3);
