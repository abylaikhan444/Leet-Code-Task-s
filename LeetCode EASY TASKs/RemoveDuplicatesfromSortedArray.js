/**
 * =============================================================================
 * НАЗВАНИЕ ЗАДАЧИ: 26. Remove Duplicates from Sorted Array (LeetCode)
 * СЛОЖНОСТЬ: Easy
 *
 * УСЛОВИЕ:
 * Дан массив целых чисел `nums`, отсортированный по неубыванию.
 * Удалите дубликаты IN-PLACE (прямо в исходном массиве, без создания нового),
 * так чтобы каждый уникальный элемент встречался только один раз.
 * Относительный порядок элементов должен сохраниться.
 *
 * Пусть k — количество уникальных элементов.
 * Функция должна вернуть число k.
 * Первые k элементов массива `nums` должны содержать уникальные значения
 * в отсортированном порядке. Что находится после индекса k-1 — не важно.
 *
 * Примеры:
 * Input:  nums = [1,1,2]
 * Output: 2,  nums = [1,2,_]
 *
 * Input:  nums = [0,0,1,1,1,2,2,3,3,4]
 * Output: 5,  nums = [0,1,2,3,4,_,_,_,_,_]
 *
 * Ограничения:
 * - 1 <= nums.length <= 3 * 10^4
 * - -100 <= nums[i] <= 100
 * - массив отсортирован по неубыванию
 *
 * ВАЖНО: нельзя создавать новый массив — менять нужно сам `nums`.
 * =============================================================================
 */

/**
 * @param {number[]} nums
 * @return {number}
 */
var removeDuplicates = function (nums) {
  let k = 1;
  for (let i = 1; i < nums.length; i++) {
    if (nums[i] !== nums[k - 1]) {
      nums[k] = nums[i];
      k++;
    }
  }
  return k;
};

removeDuplicates([0, 1, 1, 1, 2, 2, 3, 3, 4]);
